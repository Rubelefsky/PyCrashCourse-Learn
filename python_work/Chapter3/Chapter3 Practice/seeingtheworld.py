# 3.8 Store a list of 5 locations in the world to visit.
locations = ['Italy', 'Spain', 'Japan', 'Morocco']
print(locations) # Print List

print("\nThis is the sorted list of locations:")
print(sorted(locations))

print("\nThis it the original printed again:")
print(locations)

locations.reverse() # List printed in reverse
print(locations)

locations.reverse() # Change order again
print(locations)

locations.sort() # Sort in Alphabetical order
print(locations)
