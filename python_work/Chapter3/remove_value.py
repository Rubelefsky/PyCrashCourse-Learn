# Remove by a value
motorcycles = ['honda', 'ducati', 'yamaha', 'bmw']
print(motorcycles)

motorcycles.remove('bmw') # Remove the BMW Value
print(motorcycles)

too_expensive = 'ducati' # Creates the variable too_expensive with the ducati value
motorcycles.remove(too_expensive) # When removes the too_expensive value which is ducati

print(motorcycles)

print(too_expensive)

print(f"\nA {too_expensive.title()} is too expensive for me.")

