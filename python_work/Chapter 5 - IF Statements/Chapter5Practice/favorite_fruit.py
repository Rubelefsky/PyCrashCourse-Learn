# 5.7 Make a list of your favorite fruits, and then write a series of independent if statements that check for certain fruits in your list.
favorite_fruits = ['strawberry', 'blueberry', 'banana']

if 'strawberry' in favorite_fruits: # IF strawberry in list print I love strawberries
    print('I love strawberries.')
if 'blueberry' in favorite_fruits:
    print("I love blueberries.")
if 'banana' in favorite_fruits:
    print("I love bananas.")
else:
    print("Those are the only fruits I love.")