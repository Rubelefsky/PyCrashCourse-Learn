# 5.5 Turn your if-else chain from Exercise 5-4 into an if-elif- else chain.
alien_color = 'yellow' # Assign green color
if alien_color == 'green':
    print(f"Player earned 5 points.") # IF color == green, earn 5 points
elif alien_color == 'yellow':
    print(f"Player earned 10 points.") # IF color anything else, earn 10 points
elif alien_color == 'red':
    print("Player earned 15 points.")
