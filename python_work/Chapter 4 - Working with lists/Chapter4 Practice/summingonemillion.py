numbers = list(range(1, 1000001)) # Create a variable that is a list with a range of 1 - 1000001. 
print(min(numbers)) #Print the min number of the list
print(max(numbers)) # PRint the max number of the list

max_number = max(numbers) # create a variable for the max number
min_number = min(numbers) # Create a variable for the min number

print(min_number + max_number) # add both variables together to get 1000001




