# 4.1 Slices
animals = ['jackrabbit', 'horse', 'tiger', 'lion']
print(animals)

print("The first three items in the list are:")
print(animals[:3]) # Prints first three animals

print("Three items in the middle of the list are:")
print(animals[1:3]) # Prints the middle animals

print("The last three items in the list are:")
print(animals[-3:]) # prints the last three

